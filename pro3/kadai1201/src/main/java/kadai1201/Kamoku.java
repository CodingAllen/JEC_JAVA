package kadai1201;

public class Kamoku {
	private String kamokuNo;
	private String kamokuName;
	public String getKamokuNo() {
		return kamokuNo;
	}
	public void setKamokuNo(String kamokuNo) {
		this.kamokuNo = kamokuNo;
	}
	public String getKamokuName() {
		return kamokuName;
	}
	public void setKamokuName(String kamokuName) {
		this.kamokuName = kamokuName;
	}
	

}
